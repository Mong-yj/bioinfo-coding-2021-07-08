in_num1 = int(input("Enter a integer: "))
in_num2 = int(input("Enter another: "))
if in_num1 > in_num2:
    print("{} is greater than {}.".format(in_num1, in_num2))
elif in_num1 < in_num2:
    print("{} is less than {}.".format(in_num1, in_num2))
elif in_num1 == in_num2:
    print("{} is equal to {}.".format(in_num1, in_num2))

